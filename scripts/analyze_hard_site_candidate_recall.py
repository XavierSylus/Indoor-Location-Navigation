def resolve_path(path_value: str) -> Path:
    path = Path(path_value)
    return path if path.is_absolute() else PROJECT_ROOT / path


def load_yaml(path: Path) -> dict:
    with open(path, "r", encoding="utf-8") as handle:
        data = yaml.safe_load(handle)
    if not isinstance(data, dict):
        raise ValueError(f"Invalid YAML payload: {path}")
    return data


def load_json(path: Path) -> dict:
    with open(path, "r", encoding="utf-8") as handle:
        data = json.load(handle)
    if not isinstance(data, dict):
        raise ValueError(f"Invalid JSON payload: {path}")
    return data


def load_prediction_artifact(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(
            f"Missing absolute holdout point prediction artifact: {path}\n"
            f"Run first: {ARTIFACT_GENERATION_COMMAND}"
        )

    data = pd.read_csv(path)
    missing_columns = sorted(PREDICTION_ARTIFACT_COLUMNS - set(data.columns))
    if missing_columns:
        raise ValueError(
            f"Prediction artifact is missing required columns: {missing_columns}\n"
            f"Artifact: {path}"
        )

    text_columns = ["site_id", "path_id", "floor", "model_kind", "feature_source"]
    for column in text_columns:
        data[column] = data[column].astype(str)

    numeric_columns = ["timestamp", "point_index", "x", "y", "pred_x", "pred_y"]
    for column in numeric_columns:
        data[column] = pd.to_numeric(data[column], errors="coerce")

    if data[numeric_columns].isna().any().any():
        bad_columns = [column for column in numeric_columns if data[column].isna().any()]
        raise ValueError(f"Prediction artifact has non-numeric values in columns: {bad_columns}")

    data["timestamp"] = data["timestamp"].astype(np.int64)
    data["point_index"] = data["point_index"].astype(np.int64)
    return data


def get_path_predictions(predictions: pd.DataFrame, site_id: str, path_id: str) -> pd.DataFrame:
    path_df = predictions[
        (predictions["site_id"] == str(site_id))
        & (predictions["path_id"] == str(path_id))
    ].copy()
    if path_df.empty:
        raise ValueError(
            f"Prediction artifact has no rows for site_id={site_id}, path_id={path_id}.\n"
            f"Regenerate the artifact with matching holdout settings.\n"
            f"Run first: {ARTIFACT_GENERATION_COMMAND}"
        )
    return path_df.sort_values(["point_index", "timestamp"]).reset_index(drop=True)


