def collect_same_floor_training_waypoints(train_files: List[Path], floor: str) -> np.ndarray:
    points: List[List[float]] = []
    for path_file in train_files:
        if path_file.parent.name != floor:
            continue
        with open(path_file, "r", encoding="utf-8") as handle:
            for line in handle:
                if not line or line.startswith("#"):
                    continue
                parts = line.rstrip("\n").split("\t")
                if len(parts) >= 4 and parts[1] == "TYPE_WAYPOINT":
                    try:
                        points.append([float(parts[2]), float(parts[3])])
                    except ValueError:
                        continue
    if not points:
        return np.zeros((0, 2), dtype=np.float64)
    return np.unique(np.round(np.asarray(points, dtype=np.float64), 1), axis=0)


def _coord_key(floor: str, x_value: float, y_value: float) -> Tuple[str, float, float]:
    return (str(floor), float(np.round(float(x_value), 1)), float(np.round(float(y_value), 1)))


def _extract_basic_wifi_matrix(
    wifi_df: pd.DataFrame,
    query_timestamps: Sequence[int],
    bssid_vocab: Sequence[str],
    window_ms: int,
    missing_value: float,
) -> np.ndarray:
    n_rows = len(query_timestamps)
    n_bssid = len(bssid_vocab)
    matrix = np.full((n_rows, n_bssid), missing_value, dtype=np.float32)
    if n_rows == 0 or n_bssid == 0 or wifi_df is None or wifi_df.empty:
        return matrix

    bssid_to_col = {bssid: idx for idx, bssid in enumerate(bssid_vocab)}
    wifi_vocab = wifi_df[wifi_df["bssid"].isin(bssid_to_col)].copy()
    if wifi_vocab.empty:
        return matrix

    wifi_vocab = wifi_vocab.sort_values("timestamp")
    wifi_ts = wifi_vocab["timestamp"].to_numpy(dtype=np.int64)
    wifi_bssid = wifi_vocab["bssid"].to_numpy()
    wifi_rssi = wifi_vocab["rssi"].to_numpy(dtype=np.float32)

    for row_index, query_ts in enumerate(np.asarray(query_timestamps, dtype=np.int64)):
        pos = np.searchsorted(wifi_ts, query_ts, side="left")
        candidate_positions: List[int] = []
        if pos > 0:
            candidate_positions.append(pos - 1)
        if pos < len(wifi_ts):
            candidate_positions.append(pos)
        if not candidate_positions:
            continue

        diffs = np.abs(wifi_ts[candidate_positions] - query_ts)
        nearest_pos = candidate_positions[int(np.argmin(diffs))]
        nearest_ts = wifi_ts[nearest_pos]
        if abs(int(nearest_ts) - int(query_ts)) > int(window_ms):
            continue

        scan_mask = wifi_ts == nearest_ts
        for bssid, rssi in zip(wifi_bssid[scan_mask], wifi_rssi[scan_mask]):
            col = bssid_to_col.get(bssid)
            if col is None:
                continue
            if matrix[row_index, col] == missing_value or rssi > matrix[row_index, col]:
                matrix[row_index, col] = rssi

    return matrix


def extract_basic_wifi_vectors_for_paths(
    path_files: Sequence[Path],
    bssid_vocab: Sequence[str],
    window_ms: int,
    missing_value: float = MISSING_WIFI_RSSI,
) -> Tuple[pd.DataFrame, np.ndarray]:
    meta_frames: List[pd.DataFrame] = []
    vector_blocks: List[np.ndarray] = []

    for path_file in path_files:
        sensor_data = read_data_file(path_file)
        if sensor_data.waypoint is None or sensor_data.waypoint.empty:
            continue

        waypoints = sensor_data.waypoint.sort_values("timestamp").reset_index(drop=True)
        timestamps = waypoints["timestamp"].to_numpy(dtype=np.int64)
        vectors = _extract_basic_wifi_matrix(
            wifi_df=sensor_data.wifi,
            query_timestamps=timestamps,
            bssid_vocab=bssid_vocab,
            window_ms=window_ms,
            missing_value=missing_value,
        )
        meta_frames.append(
            pd.DataFrame(
                {
                    "floor": path_file.parent.name,
                    "path_id": path_file.stem,
                    "timestamp": timestamps,
                    "point_index": np.arange(len(waypoints), dtype=np.int64),
                    "x": waypoints["x"].to_numpy(dtype=np.float64),
                    "y": waypoints["y"].to_numpy(dtype=np.float64),
                }
            )
        )
        vector_blocks.append(vectors)

    if not meta_frames:
        return pd.DataFrame(), np.zeros((0, len(bssid_vocab)), dtype=np.float32)

    return pd.concat(meta_frames, ignore_index=True), np.vstack(vector_blocks)


def build_candidate_wifi_index(
    train_meta: pd.DataFrame,
    train_wifi_vectors: np.ndarray,
    missing_value: float = MISSING_WIFI_RSSI,
) -> Dict[str, object]:
    if len(train_meta) != len(train_wifi_vectors):
        raise ValueError(f"WiFi train meta/vector mismatch: meta={len(train_meta)}, vectors={len(train_wifi_vectors)}")

    candidate_vectors: Dict[Tuple[str, float, float], np.ndarray] = {}
    candidate_source_count: Dict[Tuple[str, float, float], int] = {}
    candidate_visible_count: Dict[Tuple[str, float, float], int] = {}
    floor_source_vectors: Dict[str, np.ndarray] = {}
    floor_source_coord_keys: Dict[str, np.ndarray] = {}

    meta = train_meta.copy()
    meta["x_round"] = np.round(meta["x"].astype(float), 1)
    meta["y_round"] = np.round(meta["y"].astype(float), 1)
    meta["_row_index"] = np.arange(len(meta), dtype=np.int64)

    for (floor, x_round, y_round), group in meta.groupby(["floor", "x_round", "y_round"], sort=False):
        key = _coord_key(str(floor), float(x_round), float(y_round))
        row_indices = group["_row_index"].to_numpy(dtype=np.int64)
        source_vectors = train_wifi_vectors[row_indices]
        observed = source_vectors != missing_value
        if source_vectors.shape[1] == 0:
            aggregate = np.zeros((0,), dtype=np.float32)
        else:
            aggregate = np.full(source_vectors.shape[1], missing_value, dtype=np.float32)
            any_observed = observed.any(axis=0)
            if any_observed.any():
                aggregate[any_observed] = np.max(np.where(observed[:, any_observed], source_vectors[:, any_observed], missing_value), axis=0)

        candidate_vectors[key] = aggregate
        candidate_source_count[key] = int(len(row_indices))
        candidate_visible_count[key] = int(np.sum(aggregate != missing_value))

    for floor, group in meta.groupby("floor", sort=False):
        row_indices = group["_row_index"].to_numpy(dtype=np.int64)
        floor_source_vectors[str(floor)] = train_wifi_vectors[row_indices]
        floor_source_coord_keys[str(floor)] = np.asarray(
            [
                f"{np.round(float(row.x), 1):.1f}|{np.round(float(row.y), 1):.1f}"
                for row in group.itertuples(index=False)
            ],
            dtype=object,
        )

    return {
        "candidate_vectors": candidate_vectors,
        "candidate_source_count": candidate_source_count,
        "candidate_visible_count": candidate_visible_count,
        "floor_source_vectors": floor_source_vectors,
        "floor_source_coord_keys": floor_source_coord_keys,
    }


